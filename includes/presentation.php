<h1>Présentation de la GBAF</h1>
<p>
</em>Le Groupement Banque Assurance Français (GBAF)</em> est une fédération représentant les 6 grands groupes français :
<ul>
<li>BNP Paribas</li>
<li>BPCE</li>
<li>Crédit Agricole</li>
<li>Crédit Mutuel-CIC</li>
<li>Société Générale</li>
<li>La Banque Postale.</li>
</ul>
</p>
<p>
Même s’il existe une forte concurrence entre ces entités, elles vont toutes travailler de la même façon pour gérer près de 80 millions de comptes sur le territoire national.
</p>
<p>
Le <em>GBAF</em> est le représentant de la profession bancaire et des assureurs sur tous les axes de la réglementation financière française. Sa mission est de promouvoir l'activité bancaire à l’échelle nationale. C’est aussi un interlocuteur privilégié des pouvoirs publics.
</p>

